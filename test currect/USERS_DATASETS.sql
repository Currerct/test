/*table creation script for "USERS_DATASETS"*/


CREATE TABLE  "USERS_DATASETS" 
   (	"CURRECT_USER_ID" NUMBER, 
	"CURRECT_DATASET_ID" NUMBER, 
	"CURRENT_FLAG" VARCHAR2(1) COLLATE "USING_NLS_COMP", 
	"LAST_USER" VARCHAR2(30) COLLATE "USING_NLS_COMP", 
	"LAST_DATE" DATE
   )  DEFAULT COLLATION "USING_NLS_COMP"
   
   
   
/*sequence creation script for "USERS_DATASETS"*/

   
 CREATE SEQUENCE   "USERS_DATASETS_SEQ"  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  GLOBAL
 
 
 
/*trigger creation script for "USERS_DATASETS"*/


CREATE OR REPLACE EDITIONABLE TRIGGER  "USERS_DATASETS_IUTR" 
BEFORE INSERT or update
ON users_datasets
REFERENCING NEW AS NEW OLD AS OLD
FOR EACH ROW
DECLARE
BEGIN

	:NEW.last_date := SYSDATE;

	if :NEW.last_user is null
	then
		:NEW.last_user := NVL(v('APP_USER'), USER);
	end if;	

EXCEPTION

	WHEN OTHERS THEN
		Raise_Application_Error(-20101,'Unexpected system error at level iutr(User Dataset) : '||SQLCODE||' --- '||SQLERRM, TRUE);

END users_datasets_IUTR;

/
ALTER TRIGGER  "USERS_DATASETS_IUTR" ENABLE
/

